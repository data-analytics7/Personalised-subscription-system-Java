package magazine;

/**
 * Contains the supplement details
 */
public class Supplement 
{
    /*Monthly cost of different magazine supplements*/
    private double nature = 2;
    private double tech = 2.5;
    private double health = 4.5;
    private double fashion = 3;
    private double trade = 2;
    private double research = 3.5;
    private double beauty = 5;
    
    /**
     * 
     * @return Price of nature supplement
     */
    public double getNature()
    {
        return nature;
    }
    
    /**
     * 
     * @return Price of tech supplement
     */
    public double getTech()
    {
        return tech;
    }
    
    /**
     * 
     * @return Price of health supplement
     */
    public double getHealth()
    {
        return health;
    }
    
    /**
     * 
     * @return Price of fashion supplement
     */
    public double getFashion()
    {
        return fashion;
    }
    
    /**
     * 
     * @return Price of research supplement
     */
    public double getResearch()
    {
        return research;
    }
    
    /**
     * 
     * @return Price of beauty supplement
     */
    public double getBeauty()
    {
        return beauty;
    }
    
    /**
     * 
     * @return Price of trade supplement
     */
     public double getTrade()
    {
        return trade;
    }
   
    
}


